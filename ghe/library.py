class MathLibrary:
    """A really great math library"""

    def __init__(self, x: int) -> None:
        """
        Constructor for the math library, taking a single argument to store as x
        
        :param x: the base x value to store for later
        """

    def add(self, y: int) -> int:
        """
        Calculates the real summation of the stored x and the argument y.
        :param y: integer addend 2

        :returns: The integer summation of x and y.
        """
        return self.x + y
